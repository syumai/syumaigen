var Pattern [][]int

func init() {
	Pattern = parsePattern(patternStr)
}

func parsePattern(s string) [][]int {
	lines := strings.Split(s, "\n")
	p := make([][]int, len(lines))
	for i, line := range lines {
		p[i] = make([]int, len(line))
		for j, r := range line {
			p[i][j] = int(r - '0')
		}
	}
	return p
}
